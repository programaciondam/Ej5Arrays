public class Descuento {
    private int limite;
    private Float descuento;

    public Descuento(int limite, Float descuento) {
        this.limite = limite;
        this.descuento = descuento;
    }

    public int getLimite() {
        return limite;
    }

    public Float getDescuento() {
        return descuento;
    }
    
    
}
